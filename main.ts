function displayLeaderboard (scorelist: number[]) {
    // Keeps the values to be displayed on the score board updated.
    for (let index = 0; index <= combineddisplay.length - 1; index++) {
        leaderboardDisplay = "" + leaderboardDisplay + combineddisplay[index] + "\n"
    }
    game.splash("Here is your score history:")
    game.showLongText(leaderboardDisplay, DialogLayout.Center)
    highScore = 0
    // Calculate the high score
    for (let value of scorelist) {
        if (value > highScore) {
            highScore = value
        }
    }
    if (info.score() >= highScore) {
        effects.confetti.startScreenEffect(1000)
        game.splash("Congrats, you got the high score!")
    } else {
        game.splash("Try to get the high score next time!")
    }
}
